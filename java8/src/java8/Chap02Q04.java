package java8;

public class Chap02Q04 {

	public static void main(String[] args) {
		
		int a = 8;
		int b = 5;
		
		int sum = a + b;
		int min = a - b;
		int gob = a * b;
		int na = a / b;
		int na2 = a % b;
		
		System.out.println("a + b : " + sum);
		System.out.println("a - b : " + min);
		System.out.println("a * b : " + gob);
		System.out.println("a / b : " + na);
		System.out.println("a % b : " + na2);

	}

}
