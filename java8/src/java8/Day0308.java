package java8;

public class Day0308 {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		a += b;
		
		System.out.println(a);
		
		a -= b;
		
		System.out.println(a);
		
		a *= b;
		
		System.out.println(a);
		
		a = 10;
		a *= 3 + 10;
		
		System.out.println(a);
		
		a += 1;
		
		System.out.println(a);
		
		

	}

}
